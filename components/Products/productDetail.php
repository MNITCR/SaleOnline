<script>
    document.addEventListener("DOMContentLoaded", function() {
        var amountAdd = document.getElementById("amountAdd");
        var subtractAmount = document.getElementById("subtractAmount");
        var plusAmount = document.getElementById("plusAmount");

        function updateAmount(amount) {
            // Ensure the amount is always displayed with a leading zero if less than 10
            if (amount < 10) {
                amountAdd.textContent = "0" + amount;
            } else {
                amountAdd.textContent = amount;
            }
        }

        subtractAmount.addEventListener("click", function() {
            var currentAmount = parseInt(amountAdd.textContent, 10);
            if (currentAmount > 1) {
                updateAmount(currentAmount - 1);
            }
        });

        plusAmount.addEventListener("click", function() {
            var currentAmount = parseInt(amountAdd.textContent, 10);
            updateAmount(currentAmount + 1);
        });
    });

    function swapImage(imgDetail) {
        var mainDetailImg = document.getElementById("mainDetailImg");

        var tempSrc = mainDetailImg.src;
        mainDetailImg.src = imgDetail.src;
        imgDetail.src = tempSrc;
    }
</script>
